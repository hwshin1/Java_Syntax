package org.example;

public class Main {
    public static void main(String[] args) {
        // 기본형 타입 변수
        //논리
        boolean b = true;

        //문자
        char c = 'a';

        //정수
        byte bt = 1;
        short s = 2;
        int i = 3;
        long l = 4L;

        // 실수
        float f = 5.5f;
        double d = 6.5;

        // 기본형 변수 이외의 타입들은 참조형 타입의 변수라고 한다
        // 참조형 변수는 실체가 없다 실체는 외부에 저장.
        // 참조형 변수에는 실체를 조종 할 수 있는 리모컨이 저장된다.
    }
}