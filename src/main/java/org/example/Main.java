package org.example;

public class Main {
    public static void main(String[] args) {
        String s = "8 * 1 = 8\n";
        String s2 = "8 * 2 = 16\n";
        String s3 = "8 * 3 = 24";
        System.out.println(s + s2 + s3); // String은 문자열을 저장할 수 있는 자료형이다.

        System.out.println("===========");

        System.out.println("8 * 1 = 8");
        System.out.println("8 * 2 = 16");
        System.out.println("8 * 3 = 24");

        System.out.println("===========");

        System.out.println("8 * 1 = 8\n8 * 2 = 16\n8 * 3 = 24");
    }
}