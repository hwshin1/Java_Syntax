package org.example;

public class Main {
    public static void main(String[] args) {
        // System.out은 표준출력(모니터)
        // .은 of를 의미
        // 프로그램은 위에서 아래로 한줄씩 실행
        // 프로그램 시작점
        System.out.println(1);
        System.out.println("2");
        System.out.println("3");
        System.out.println("사");
        // 프로그램 끝점
        // 출력
        // 1
        // 2
        // 3
        // 사
    }
}