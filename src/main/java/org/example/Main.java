package org.example;

public class Main {
    public static void main(String[] args) {
        // while 문
        System.out.println("==while 문==");
        int i = 1;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        // for 문
        System.out.println("==for 문==");
        for (int j = 1; j <= 10; j++) {
            System.out.println(j);
        }
    }
}