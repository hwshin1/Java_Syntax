package org.example;

public class Main {
    public static void main(String[] args) {
        //논리
        boolean b = false;

        //문자
        char c = 'b';

        //정수
        byte bt = 4;
        short s = 8;
        int i = 10;
        long l = 5L;

        // 실수
        float f = 6.21f;
        double d = 6.5415;

        System.out.println(b);
        System.out.println(c);
        System.out.println(bt);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(f);
        System.out.println(d);
    }
}